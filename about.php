	<section  id="stage" style="margin:auto; margin-top: 3%">
		<h2 align="center" style="margin-bottom:2%"><u>A Propos du stage</u></h2>
		<p>
			La série Technologies de l’Information (TI) forme les élèves dans le domaine de l’informatique professionnelle et la gestion des entreprises. Elle a été mise sur pied dans l’optique de favoriser l’insertion facile et rapide des élèves en milieu professionnel. C’est pour cette raison que ces derniers sont appelés à effectuer un stage de cinq (05) semaines allant de la période de la publication des résultats du probatoire jusqu’à la rentrée scolaire aui suit.
			C’est dans ce cadre que nous avons eu l’opportunité d’être accueillis à la Banque des Etats de l’Afrique Centrale (BEAC) 	de Bafoussam pour un stage académique durant la période allant du 05 août au 06 septembre 2019. Ce stage à la BEAC de Bafoussam nous a permis d’acquérir de nombreuses connaissances sur le plan professionnel. Il s’agit entre autres de :
			<ul type="square">
				<li>-la ponctualité,</li>
				<li>-la rigueur dans le travail,</li>
				<li>-la politesse et le respect de la hiérarchie,</li>
				<li>-l’esprit d’équipe et d’initiative,</li>
				<li>-l’expression orale en public,</li>
				<li>-etc.</li>
			</ul>
		</p>
	</section>
	<section id="appli" style="margin:auto; margin-top: 3%;margin-bottom:2%;">
		<h2  align="center" style="margin-bottom:2%"><u>A propos de l'application</u></h2>
		<p>
			Nous avons constaté qu’à l’Agence BEAC de Bafoussam, la gestion de la maintenance des installations sanitaires et de plomberie se faisait manuellement, ce qui ne permettait pas un suivi efficace. C’est dans le souci de résoudre ce problème que nous avons pensé qu’il serait bien de mettre sur pieds une application.L’application à concevoir pour la gestion de la maintenance des installations sanitaires, de plomberie et du forage au sein de la BEAC de Bafoussam est capable de :
			<ul type="square">
				<li>permettre au prestataire de se connecter pour entrer les informations dans le compte rendu technique  et soumettre le rapport;</li>
				<li>permettre aux agents de se connecter pour consulter le rapport soumis par le prestataire;</li>
				<li>alerter en cas de problème d’urgence ;</li>
				<li>fournir l’historique des interventions réalisées sur les équipements et des problèmes résolus</li>
 
		</p>
