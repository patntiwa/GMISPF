	<section id="beac" style="margin:auto; margin-top: 3% ;" >
		<h2  align="center" style="margin-bottom:2%"><u>A propos de la BEAC</u></h2>
		<p>La Banque des Etats de l’Afrique Centrale (BEAC), institution de la Communauté Economique et Monétaire de l’Afrique Centrale (CEMAC), est un établissement public multinational regroupant les six (06) Etats membres de la CEMAC. Elle est l’Institut d’Emission Monétaire d’Afrique Centrale. Ses objectifs sont : 
			<ul type="square">
 				<li>Emettre la monnaie ayant cours légal et pouvoir libératoire dans les Etats membres, </li>
 				<li>Garantir la stabilité économique et monétaire dans la zone d’émission, </li>
 				<li>Définir et conduire la politique monétaire applicable dans les pays membres de la CEMAC, </li>
 				<li>Conduire les opérations de change, </li>
 				<li>Détenir et gérer les réserves de change des pays membres, </li>
 				<li>Et enfin, de promouvoir le bon fonctionnement du système de paiement dans la CEMAC.</li>
 				</ul>
 				La BEAC est représentée au Cameroun par plusieurs agences. 
 				Dirigée par Monsieur MBOA Marcel, l’agence de Bafoussam pour mener à bien ses activités, est constituée de plusieurs services :
 				<ul type="square">
<li>Le service de la Comptabilité : il est chargé de superviser l’élaboration des différents rapports comptables, la signature des pièces comptables, la clôture périodique des opérations comptables.     </li>                          
<li>Le service des études, Balance de Paiement, et Marchés de capitaux : ce service est chargé de la prospection et de la collecte des informations, de l’étude et des notes d’informations économiques, de la validation des dossiers de crédit à soumettre aux instances de décision.</li>
<li>Le Service d’Emission Monétaire et des Mouvements de Fonds : il est chargé d’émettre la monnaie et les opérations de change,</li>
<li>Le service des Ressources Humaines et de la Formation : il assure la préparation des dossiers de recrutement, la mise en œuvre de la discipline, l’encadrement professionnel des agents, le suivi des permissions et autorisations d’absence aux agents, la supervision des formations dans le centre, etc.</li>

 		</ul>
</p>
